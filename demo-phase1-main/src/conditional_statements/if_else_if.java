package conditional_statements;

public class if_else_if {

	public static void main(String[] args) {
		
		int n=0;
		
		if(n>0)
		{
			System.out.println("num is positive ");
		}
		else if(n<0)
		{
			System.out.println("num is negative ");
		}

		else 
		{
			System.out.println("num is zero ");
		}
	}

}
