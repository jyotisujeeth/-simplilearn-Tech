package Strings;

public class string1 {

	public static void main(String[] args) {
		
		String s1 ="String Program"; //string literal 
		
		String s2 =  new String("simplilearn coding test"); //new keyword 
		
		char s3[]= {'s','t','r', 'i','n', 'g'}; //character array
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
		
		
		String s4= "delhi"+5+5; // delhi55
		System.out.println(s4);
		
		String s5= "delhi"+'5'+'5'; //delhi55
		System.out.println(s5);
		
		String s6 ="delhi"+(5+5); //delhi10
		System.out.println(s6);

	}

}
